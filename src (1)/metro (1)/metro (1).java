    private void tcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tcodeKeyPressed
        // TODO add your handling code here:
        
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            try {
                String bcode = tcode.getText();
                pst = (PreparedStatement) con.prepareStatement("select * from ticket where id = ?");
                pst.setString(1, bcode);
                rs = pst.executeQuery();
                
                if(rs.next() == false){
                    //jOptionPane.showMessageDialog(this,"ticket code not found");
                }
                else{
                    String bname = rs.getString("dname");
                    tdname.setText(bname.trim());
                    String price = rs.getString("price");
                    tprice.setText(price.trim());
                    
                    tquantity.requestFocus();
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(metro.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tcodeKeyPressed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int price = Integer.parseInt(tprice.getText());
        int qty = Integer.parseInt(tquantity.getValue().toString());
        int tot = price * qty;
        
        df = (DefaultTableModel)jTable1.getModel();
        df.addRow(new Object[]
        {
        
        tdname.getText(),
        tprice.getText(),
        tquantity.getValue().toString(),
        tot
        
        });
        
        int sum = 0;
        for(int i=0; i<jTable1.getRowCount(); i++){
            sum = sum + Integer.parseInt(jTable1.getValueAt(i, 3).toString());
        
        }
        tcost.setText(String.valueOf(sum));
        tcode.setText("");
        tdname.setText("");
        tprice.setText("");
        tquantity.setValue(0);
        tcode.requestFocus();
        
    }//GEN-LAST:event_jButton1ActionPerformed

