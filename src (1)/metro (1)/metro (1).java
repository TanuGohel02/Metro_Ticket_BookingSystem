    public void sales(){
        String totalcost = tcost.getText();
        String pay = tpay.getText();
        String bal = tbal.getText();
        
        
        int lastid = 0;
        
        
        try {
            String query = "insert into sales(subtotal,pay,bal)values(?,?,?)";
            pst = (PreparedStatement) con.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);
            pst.setString(1,totalcost);
            pst.setString(2,pay);
            pst.setString(3,bal);
            pst.executeUpdate();
            rs = pst.getGeneratedKeys();
            
            if(rs.next()){
                lastid = rs.getInt(1);
            }
            int rows = jTable1.getRowCount();
            
            String query1 = "insert into sales_tickets(ticket_id,dname,price,qty,total)values(?,?,?,?,?)";
            pst1 = (PreparedStatement) con.prepareStatement(query1);
            
            String dname ="";
            String price;
            String qty;
            int total = 0;
            
            for(int i=0; i<jTable1.getRowCount();i++){
                dname = (String)jTable1.getValueAt(i, 0);
                price = (String)jTable1.getValueAt(i, 1);
                qty = (String)jTable1.getValueAt(i, 2);
                total = (int)jTable1.getValueAt(i, 3);
                
                pst1.setInt(1, lastid);
                pst1.setString(2, dname);
                pst1.setString(3, price);
                pst1.setString(4, qty);
                pst1.setInt(5, total);
                pst1.executeUpdate();
                
            }
            JOptionPane.showMessageDialog(this, "sales completed");
            
        } catch (SQLException ex) {
            Logger.getLogger(metro.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
